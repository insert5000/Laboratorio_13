package com.example.lab13pushnotifications.Model;

public class Persona {
    private String name;
    private int age;

    public Persona() { }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }
}
